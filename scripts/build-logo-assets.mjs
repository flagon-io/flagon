// Regenerates every derived logo asset from src/lib/logo-geometry.json.
//
//   npm run logo
//
// The mark used to exist as four hand-maintained copies: logo.tsx, icon.svg,
// and both GitHub avatars. Nothing kept them in step, so a revision meant
// editing path data in four places and hoping. Now the JSON is the mark and
// everything else is output.
//
// PNGs are not produced here - they need a rasterizer, and adding a headless
// browser to a logo script is a poor trade. The SVGs are written, and the PNG
// step is reported so it stays visible rather than silently stale.
import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const root = join(here, "..");
const geometry = JSON.parse(
  readFileSync(join(root, "src/lib/logo-geometry.json"), "utf8"),
);

const { paths, strokeWidth, bounds } = geometry;
const pathTags = paths
  .map((d, i) => `    <!-- ${geometry.pathNames[i]} -->\n    <path d="${d}" />`)
  .join("\n");

const GENERATED = "Generated by scripts/build-logo-assets.mjs - do not edit.";

/** The favicon: gradient stroke, transparent ground. */
const icon = `<svg xmlns="http://www.w3.org/2000/svg" viewBox="${geometry.viewBox}" fill="none">
  <!-- ${GENERATED} -->
  <defs>
    <linearGradient id="flagon-stroke" x1="${bounds.x}" y1="${bounds.y}" x2="${bounds.x + bounds.width}" y2="${bounds.y + bounds.height}" gradientUnits="userSpaceOnUse">
      <stop stop-color="#2dd4bf" />
      <stop offset="1" stop-color="#0d9488" />
    </linearGradient>
  </defs>
  <g fill="none" stroke="url(#flagon-stroke)" stroke-width="${strokeWidth}" stroke-linejoin="round" stroke-linecap="round">
${pathTags}
  </g>
</svg>
`;

/**
 * A square avatar: the mark centred on a filled tile.
 *
 * The scale and offset are computed from `bounds` rather than written down.
 * The previous avatars hardcoded a translate for the OLD art's centre, so any
 * change to the mark left it sitting off-centre inside the tile.
 */
function avatar({ background, stroke, inset = 0.6 }) {
  const cx = bounds.x + bounds.width / 2;
  const cy = bounds.y + bounds.height / 2;
  const scale = (64 * inset) / Math.max(bounds.width, bounds.height);
  return `<svg xmlns="http://www.w3.org/2000/svg" width="512" height="512" viewBox="${geometry.viewBox}" fill="none">
  <!-- ${GENERATED} -->
  ${background}
  <g transform="translate(32 32) scale(${scale.toFixed(4)}) translate(${-cx} ${-cy})" fill="none" stroke="${stroke}" stroke-width="${strokeWidth}" stroke-linejoin="round" stroke-linecap="round">
${pathTags}
  </g>
</svg>
`;
}

const tealTile = `<defs>
    <linearGradient id="bg" x1="0" y1="0" x2="0" y2="64" gradientUnits="userSpaceOnUse">
      <stop stop-color="#2dd4bf" />
      <stop offset="1" stop-color="#0d9488" />
    </linearGradient>
  </defs>
  <rect width="64" height="64" fill="url(#bg)" />`;

const outputs = [
  ["src/app/icon.svg", icon],
  [
    ".github/avatar-teal.svg",
    avatar({ background: tealTile, stroke: "#ffffff" }),
  ],
  [
    ".github/avatar-dark.svg",
    avatar({
      background: '<rect width="64" height="64" fill="#09090b" />',
      stroke: "#2dd4bf",
    }),
  ],
];

for (const [rel, contents] of outputs) {
  const target = join(root, rel);
  mkdirSync(dirname(target), { recursive: true });
  writeFileSync(target, contents, "utf8");
  console.log(`  wrote   ${rel}`);
}

console.log(
  "\nSVGs regenerated. PNGs (.github/avatar-*.png, public/email/flagon-mark.png)" +
    "\nare rasterized separately - re-export them when the mark changes.",
);
